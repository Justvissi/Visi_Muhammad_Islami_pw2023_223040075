                    <h2>'Blue Lock' Mendapat Season Kedua</h2>
                    <p>
                    Musim pertama yang diproduksi oleh studio animasi 8bit mulai ditayangkan pada 9 Oktober. Crunchyroll menyiarkan secara bersamaan serial tersebut dengan subtitle dan pangkat bahasa Inggris.

Blue Lock mengadaptasi manga sepak bola karya Muneyuki Kaneshiro dan Yuusuke Nomura, yang dimulai di Weekly Shounen Magazine pada Agustus 2018. Kodansha menerbitkan volume ke-23 pada 16 Maret. Kodansha USA melisensikan manga tersebut dalam bahasa Inggris pada Januari 2021 dan merilis volume ke-18 pada 21 Maret.

Kaneshiro dan Kouta Sannomiya mulai membuat serial manga spin-off yang berfokus pada Seishirou Nagi di Majalah Bessatsu Shounen pada 9 Juni. Kodansha mengirimkan volume keduanya pada 16 Maret.

Blue Lock memenangkan Penghargaan Manga Kodansha ke-45 di divisi Shounen pada Mei 2021. Manga ini juga menduduki puncak kategori Olahraga dalam Komik Rekomendasi edisi ke-15 yang Dipilih oleh Karyawan Toko Buku Jepang.
