                    <h2>'Boku no Hero Academia' Musim ke-7 dalam Produksi</h2>
                    <p>
                    Episode ke-25 dan terakhir dari musim keenam Boku no Hero Academia (My Hero Academia) diakhiri dengan pengumuman pada hari Sabtu bahwa musim ketujuh untuk anime TV sedang diproduksi. Situs web resmi juga memposting pengumuman promo dan visual (foto).

Aktris pengisi suara Romi Park (Shaman King, Deadman Wonderland) bergabung sebagai Star and Stripe.

Serial anime ini mengadaptasi manga superhero aksi Kouhei Horikoshi, yang terinspirasi dari one-shot Boku no Hero (Pahlawanku) yang diterbitkan dalam Akamaru Jump edisi Musim Dingin 2008. Manga ini memulai serialisasi di Weekly Shounen Jump pada bulan Juli 2014. Shueisha menerbitkan volume ke-37 pada tanggal 3 Februari. Boku no Hero Academia memiliki 85 juta eksemplar kumulatif dari volume kompilasinya yang dicetak.

Diproduksi oleh Bones, anime televisi pertama ditayangkan dalam 13 episode pada Musim Semi 2016. Musim berturut-turut ditayangkan perdana pada Musim Semi 2017, Musim Semi 2018, Musim Gugur 2019, dan Musim Semi 2021, masing-masing ditayangkan selama 25 episode.
